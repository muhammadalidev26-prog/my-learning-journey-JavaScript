"use strict";



// PROMPT
// It shows a modal window with a text message, an input field for the visitor, and the buttons OK/Cancel.
// It takes two parameters, the title (that is shown to the user) and the default value (if user clicks ok without typing, the default value weould be assigned to the variable if user clicks cancel then Null value would be assigned to the variable)
// the Default Parameter is optional.

let age = prompt("How old are you?", 100);

alert(`You are ${age} years old!`); // You are 100 years old!



